package com.report.example;

public class Report {
    
    private String nome;
    private String dataNascimento;
    private String matricula;
    private String cargo;
    private String remuneracao;
    private String dataAdmissao;
    private String cpf;
    private String nomeMae;
    private String linguagensProgramacao;
    private String funcionarioImg;


    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public String getDataNascimento() {
        return dataNascimento;
    }
    public void setDataNascimento(String dataNascimento) {
        this.dataNascimento = dataNascimento;
    }
    public String getMatricula() {
        return matricula;
    }
    public void setMatricula(String matricula) {
        this.matricula = matricula;
    }
    public String getCargo() {
        return cargo;
    }
    public void setCargo(String cargo) {
        this.cargo = cargo;
    }
    public String getRemuneracao() {
        return remuneracao;
    }
    public void setRemuneracao(String remuneracao) {
        this.remuneracao = remuneracao;
    }
    public String getDataAdmissao() {
        return dataAdmissao;
    }
    public void setDataAdmissao(String dataAdmissao) {
        this.dataAdmissao = dataAdmissao;
    }
    public String getCpf() {
        return cpf;
    }
    public void setCpf(String cpf) {
        this.cpf = cpf;
    }
    public String getNomeMae() {
        return nomeMae;
    }
    public void setNomeMae(String nomeMae) {
        this.nomeMae = nomeMae;
    }
    public String getLinguagensProgramacao() {
        return linguagensProgramacao;
    }
    public void setLinguagensProgramacao(String linguagensProgramacao) {
        this.linguagensProgramacao = linguagensProgramacao;
    }
    public String getFuncionarioImg() {
        return this.funcionarioImg;
    }
    public void setFuncionarioImg(String funcionarioimg) {
        this.funcionarioImg = funcionarioimg;
    }
    

}
